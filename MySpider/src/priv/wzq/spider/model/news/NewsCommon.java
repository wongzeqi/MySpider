package priv.wzq.spider.model.news;

public class NewsCommon {
	private int nid;//新闻id
	private String common;//评论
	private String good;//赞
	public int getNid() {
		return nid;
	}
	public void setNid(int nid) {
		this.nid = nid;
	}
	public String getCommon() {
		return common;
	}
	public void setCommon(String common) {
		this.common = common;
	}
	public String getGood() {
		return good;
	}
	public void setGood(String good) {
		this.good = good;
	}
	
}
